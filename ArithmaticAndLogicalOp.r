 #Demonstrate Arithmetic and Logical Operations with simple examples.
 x=4
 y=2
 #Arithmetic Operations
 addition_result<-x+y
 addition_result

 subtraction_result<-x-y
 subtraction_result

 multiplication_result<-x*y
 multiplication_result

 division_result<-x/y
 division_result

 modulus_result<-x%%y
 modulus_result

 #Logical Operations
 is_equal<-x==y
 is_equal

 is_grater_than<-x>y
 is_grater_than

 is_not_equal <-x!=y
 is_not_equal

 logical_and<-TRUE&FALSE
 logical_and

logical_or<-TRUE|FALSE
 logical_or

 logical_not<-!TRUE
 logical_not
